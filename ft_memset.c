#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int		i;
	char	*b2;

	b2 = b;
	i = 0;
	while (i < (int)len)
	{
		b2[i] = c;
		i++;
	}
	return (b);
}
